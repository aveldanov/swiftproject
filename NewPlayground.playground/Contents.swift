

func loveCalculator(){
    
    let loveScore = Int.random(in: 1...100)
    
    if loveScore >= 80{
        print(">=80")
    }else if loveScore >= 40{
        
        print(">=40")
    }else{
        
        print("<40")
    }
    
    
    
}

loveCalculator()


























